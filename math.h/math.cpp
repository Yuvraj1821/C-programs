#include<stdio.h>
#include<math.h>
main()
{
	printf("\n ceil : %f",ceil(3.6));	// it round up the value

	printf("\n floor : %f",floor(3.3));  // it round down the value
	
	printf("\n sqrt : %f",sqrt(6));  
	
	printf("\n Power : %f",pow(2,5)); 
	
	printf("\n Absolute number : %d",abs(-10));  // it prints the absolute value
}
